7) Faça um algoritmo em PHP onde verifica se o valor da variável A é maior ou menor que o valor da variável B.
A mensagem a ser impressa deve ser “A maior que B” ou “A menor que B”.

<?php
  $numA = 110;
  $numB = 12;

  if($numA > $numB){
    echo "A maior que B";
  }else{
    echo "A menor que B";
  }
?>